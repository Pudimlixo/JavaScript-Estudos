/*Operadores relacionais em JS
  == -> Igual
  === -> Estritamente igual
  != -> Diferente
  !== -> Estritamente diferente
  > -> maior
  < -> Menor
  >= -> Maior ou igual
  <= -> menor ou igual
*/


//Tais operadores relacionais servem para que a resposta de suas expressões desviem ou não o fluxo do programa mediante uma ação


/*
    Operadores relacionais de identidade em JS:
    === -> Estritamente igual (Testa a grandeza e tipo dos operandos)
    !== -> Desigual restrito (Testa se os operandos tem grandeza e tipos diferentes)
*/

/*
    Operadores lógicos em JS(Em ordem de precedência):
    ! -> NOT, Expressão de negação unária que troca valor de uma expressão booleana
    && -> AND, Expressão binária que muda fluxo do programa com o seu resultado booleano tendo que ter todas as condições true
    || -> OR, Expressão binária que muda fluxo do programa com o seu resultado booleano podendo ter somente uma das condições true que já faz essa mudança de fluxo
*/

/* 
    Operador ternário em JS - Operador com três operandos que funciona como uma estrutura de controle básica
    (Expressão testada)?(Bloco executado se expressão for verdadeira):(Bloco executado se expressão for falsa)
*/
