//Teoria sobre métodos em vetores no JS

//Métodos que já conheço:

//.push() -> adiciona novo valor e posição em qualquer vetor

//.reverse() -> inverte a posição dos índices de um vetor e os valores desses índices

//.indexOf(x) -> preocura índice do valor que está como parâmetro desse método usado em qualquer vetor

//.find() -> executa um determinado fluxo de código se um valor especifíco for encontrado no array

//.filter() -> retorna somente objetos e valores de um array se eles satisfazerem uma condição específica

//.map() -> percorrer cada posição de um array e retornar um novo array com valores que dependem do processamento que uma função fez no primeiro array e o que essa função mudou nele para basear valores do segundo array

//.ForEach() -> percorre todas as posições e faz ser exedcutada uma funçõ para cada elemento presente no array, sem criar um novo vetor

//.some() -> analisa todos os elementos de um array e verifica se um deles cumpre uma condição, retornando simplesmente "true" ou "false", é mais usado para determinar fluxo de um programa

//.every() -> tem a mesma função que o some(), entretando ele verifica se todos os elementos do array cumprem a condição da função

//.reduce() -> reduz todos os elementos de um array em somente o valor de uma variável, que é determinado pela condição que a função passa

//.includes() -> verifica se o valor passado como argumento desse método tem no array, retornando true ou false

//.pop() -> exclui o último elemento de um array e retorna aquele valor para o usuário